/*******************************************************************************************
 ** @brief: support_crc16
 ** @param: pData, nLength
 *******************************************************************************************/
extern uint16 support_crc16( const uint8* pData, uint32 nLength )
{
	uint16 crc16 = 0;
	uint32 k =0 ;
	
	while(nLength > 0)
	{
		k = (crc16 ^ *pData) & 0xff;
		crc16 = (crc16 >> 8) ^ crctab16[k];
		nLength--;
		pData++;
	}
	return (~crc16);
}
