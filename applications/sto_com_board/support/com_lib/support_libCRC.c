/*******************************************************************************************
 ** @brief: crc16_X16152
 ** @param: pData, nLength, crc_init
 *******************************************************************************************/
static uint16 crc16_X16152( uint8 *pData,uint32 nLength, uint16 crc_init )
{
	uint16 a = 0,b = 0,v = 0,CRC_16=0xFFFF;//CRC初值
	 
	CRC_16 = crc_init;
	for(a =0;a < nLength;a++)
	{
		CRC_16 ^= pData[a];
		for(b =0;b <8;b++)
		{
			if(CRC_16&0x01)
			{
				CRC_16 = CRC_16 >> 1;
				CRC_16 = CRC_16 ^ 0xA001;
			}
			else
			{
				CRC_16 = CRC_16 >> 1;
			}
		 }
	 }
	 v = ((CRC_16 & 0x00ff) << 8) | ((CRC_16 & 0xff00) >> 8);
	 return v;
}

